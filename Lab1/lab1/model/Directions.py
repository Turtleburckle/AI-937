class DirectionsClass:

    # Directions Class contains all the directions used all over the program.
    def __init__(self):
        self.up = 0
        self.left = 1
        self.down = 2
        self.right = 3

    def getUp(self):
        return self.up

    def getDown(self):
        return self.down

    def getLeft(self):
        return self.left

    def getRight(self):
        return self.right
