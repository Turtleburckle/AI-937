class ColorsClass:

    def __init__(self):
        self.blue = (0, 0, 255)
        self.grayBlue = (50, 120, 120)
        self.red = (255, 0, 0)
        self.green = (0, 255, 0)
        self.black = (0, 0, 0)
        self.white = (255, 255, 255)

    def getBlueColor(self):
        return self.blue

    def getGrayBlueColor(self):
        return self.grayBlue

    def getRedColor(self):
        return self.red

    def getGreenColor(self):
        return self.green

    def getBlackColor(self):
        return self.black

    def getWhiteColor(self):
        return self.white
